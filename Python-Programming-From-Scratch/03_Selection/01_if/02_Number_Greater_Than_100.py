# Problem Statement:
# Write a Python program that accepts a number from the user.
#
# If the entered number is greater than 100,
# display:
# Number is greater than 100
#
# Note:
# Do not use an else block in this program.
#
# Example:
#
# Input:
# Enter Number : 150
#
# Output:
# Number is greater than 100
# ============================================================

number = int(input("Enter Number : "))

if number > 100:
    print("Number is greater than 100 :) ")