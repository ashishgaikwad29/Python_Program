#
# Problem Statement:
# Write a Python program that accepts the length and breadth
# of a rectangle from the user.
#
# Calculate the area of the rectangle and display the result.
#
# Formula:
# Area = Length × Breadth
#
# ============================================================

length = int(input("Enter Length : "))
breadth = int(input("Enter Breadth : "))

area = length * breadth

print("Area is : ",area)
